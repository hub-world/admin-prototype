export function randomFromArray<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)];
}

export function generateEmail(firstName: string, lastName: string): string {
  const domain = randomFromArray(emailDomains);
  return `${firstName.toLowerCase()}.${lastName.toLowerCase()}@${domain}`;
}

export function generatePhone(nationality: string): string {
  const prefix = phoneCountryPrefixes[nationality] || "+49 ";
  const number = Math.floor(Math.random() * 900000000 + 100000000);
  return prefix + number;
}
